uint64
sys_exit(void)
{
  int n;
  argint(0, &n);
  exit(n);
  return 0;  // not reached
}

uint64
sys_getpid(void)
{
  return myproc()->pid;
}

uint64
sys_fork(void)
{
  return fork();
}

uint64
sys_wait(void)
{
  uint64 p;
  argaddr(0, &p);
  return wait(p);
}

uint64
sys_sbrk(void)
{
  uint64 addr;
  int n;

  argint(0, &n);
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

uint64
sys_sleep(void)
{
  int n;
  uint ticks0;

  argint(0, &n);
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(killed(myproc())){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

uint64
sys_kill(void)
{
  int pid;

  argint(0, &pid);
  return kill(pid);
}

// return how many clock tick interrupts have occurred
// since start.
uint64
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

//returns the path of the cwd inode stored within myproc() by getting its dirent name,
//and copies it out to user space to a user provided buffer.
uint64
sys_cwd(void)
{
  int size;
  uint64 addr;
  argaddr(0, &addr);
  argint(1, &size);

  struct dirent de;
  struct inode *pwd = myproc()->cwd;
  pagetable_t pagetable = myproc()->pagetable;
  readi(pwd, 0, (uint64)&de, 0, sizeof(de));
  if(sizeof(de.name) > size){
  	return -1;
  }
  return copyout(pagetable, addr, de.name, sizeof(de.name));
}
